#include <stdio.h>
#include <unistd.h>

char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	unsigned int	i;

	i = 0;
	while (src[i] != '\0' && i < n - 1)
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}

int	main(void)
{
	char	dest[10];

	printf("%lu\n", sizeof(dest));
	printf("%s", ft_strncpy(dest, "You should see this.", sizeof(dest)));
	return (0);
}
